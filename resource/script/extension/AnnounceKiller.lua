function AnnounceKiller(ignore, mob, player)
    local mobID = GetChaID(mob)
	
	if(MonsterAnnounce[mobID] ~= nil)then

		local repu_award = MonsterAnnounce[mobID].Rep
		local gold_award = MonsterAnnounce[mobID].Gold
		
		if repu_award > 0 then
			GiveReputation(player, repu_award)
		end
		if gold_award > 0 then
			GiveGold(player, gold_award)
		end
		
		if MonsterAnnounce[mobID].announce then
			local mob_name = GetChaDefaultName(mob)
			local player_name = GetChaDefaultName(player)
			ScrollNotice( "["..mob_name.."] was killed by "..player_name.."!!, who won "..repu_award.." reputation and "..gold_award.." Gold!",1)
		end
	end
	
end





Hook:AddPostHook("GetExp_PKM", AnnounceKiller)